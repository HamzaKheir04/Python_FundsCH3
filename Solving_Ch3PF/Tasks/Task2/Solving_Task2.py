# Explanation of append method with Example :
# We use 'Append' Method to adds a single element to the end of the list 

List = [1,2,3]
print(f"The List Before Using the Append : {List}")
List.append(4)
print(f"The List After Using Append : {List}")

# As we can see the (4) add to the end of the list so the list become [1, 2, 3, 4]